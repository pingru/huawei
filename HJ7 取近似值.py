#方法2:
def fun():
    a = input()
    xs = a.split('.')[1]  # 输入是字符串类型的,按照.进行划分,取后面那部分,得到的是一个子字符串
    fx = list(xs)[0]  # 子字符串的小数部分,转化为列表,每个元素是子字符串的一个字符,取第一个字符来判断,.4999,要用4跟5比,所以要取得4
    if int(fx) >= 5:
        return int(a.split('.')[0]) + 1  # 小数部分的值,如果大于5,整数部分加1,输出整数部分
    else:
        return int(a.split('.')[0])  # 否则,整数部分不变,输出整数部分


print(fun())
